<div class="sectionBorder clearfix">
<h4>ファッションアイテムを対象とした双方向アイテム交換推薦システム</h4>
ユーザーが所持しているファッションアイテムを対象に、あるコーディネートをユーザーが評価することでそのユーザーの好みや使用頻度を推定し、ユーザー同士のアイテムの交換を推薦することを可能にする。
本研究の特徴は、他のユーザーが不要と考えているアイテムを一時的・仮想的にユーザーのヴァーチャルクローゼットに追加し、コーディネートを自動生成・評価することで、そのユーザーと交換相手のユーザーの双方にメリットのある交換プランを提案する点である。
ウェブ上でユーザー同士のアイテムの交換を促す際に、双方の満足度がより高い交換を推薦することができる。
