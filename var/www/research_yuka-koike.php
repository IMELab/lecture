<h2>研究事例：小池 悠佳</h2>

<h3>研究概要</h3>
<div class="sectionBorder clearfix">
<h4>エディトリアル・デザインに関するテンプレートを用いたクエリ入力支援機能を有するe-Books検索エンジン</h4>
本研究は，高度なレイアウト構造を有する書籍を対象として，レイアウト構造指定と色彩を指定するクエリによる書籍検索エンジンの実現方式を示す．
本研究の特徴は，エディトリアル・デザインに関する情報（レイアウト，および，配色）をあらかじめ知識ベース中に蓄積し，この知識ベースを用いて，ユーザによるクエリ入力を支援する点にある．本システムは，Webベースのスケッチによるレイアウト構造指定と，配色を選択することによって，サーバ側 に蓄積されているレイアウト構造のサンプルデータと配色のサンプルデータを用いておおまかに入力されたクエリを補完し，検索する．
本研究の利点は、このクエリ入力支援機能により，利用者は，容易に試行錯誤しながら，専門知識がなくてもデザインをスケッチし，選択をするだけで 書籍を検索することができる点である． 
</div>
