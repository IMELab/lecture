<h3>Abstract</h3>
<div class="sectionBorder">
    <h4>Design and Implementation of Query-by-Appearance System  -An Interactive Search Engine Framework for Retrieving e-Books with Visual Query Expansion methods-</h4>
This paper proposes a visual query expansion method called Query-by-Appearance, which utilizes techniques developed in the field of editorial design. Our system expands a simple query into one that is informative and well-designed one, allowing users to find their desired e-book intuitively. To simplify the selection of target media data on modern touch screen devices, our system visualizes the retrieved results in a two-dimensional plot that incorporates both layout-based and color-based relevance rankings. We conduct two experiments to evaluate the effectiveness of our Query-by-Appearance method, comparing hand-drawn and automatically generated queries to determine the extent to which the editorial design templates improve media retrieval. The results show that our visual query expansion method successfully increases retrieval performance for display-oriented media data.
</div>

