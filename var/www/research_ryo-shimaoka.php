<h2>研究事例：島岡 諒</h2>

<h3>研究概要</h3>
<div class="sectionBorder clearfix">
<h4>タブレット端末におけるウェブ検索を対象とした複合検索語ナビゲーション機能を有するタイピング・フリー検索機構</h4>
<p>本研究は、検索語と検索語の関係を「言葉の地図」としてダイナミックに可視化するエンジンを開発することにより、スマートフォンやタブレット端末などのタッチ・パネルから、検索エンジンへの入力を容易にする。
本研究の特徴は、利用者の興味遷移を、1)検索テーマを絞込む遷移（Narrowing）、および、2)検索テーマを切り替える遷移（Sliding）として捉え、検索語間の関連を、これらの利用者の二種類の興味遷移を定義する検索語相関マトリクス上において学習することにより、利用者自身の収束的・連想的な興味遷移に応じた検索語推薦を行う点にある。
これにより、利用者は、タップ操作のみによって、自身の興味の遷移に応じた情報検索を高速に行うことが出来る。</p>
