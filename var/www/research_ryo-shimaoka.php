<h3>Abstract</h3>
<div class="sectionBorder">
<h4>Cross-Domain Query Navigation<br />
- An Interactive Query Visualization Mechanism by Sharing Search Stories of Social Media -</h4>
Tablets and smartphones have become the most popular Internet client system for end-users. However, conventional web search engines employ Hyper Text Markup Language (HTML) text input systems that require many individual characters. These are unsuitable for mobile terminals, which are normally equipped with touchscreens. We propose a cross-domain query navigation and visualization system that assists the query input process by providing a context-dependent word map that presents the relevance between keywords. This word map enables both a narrowing action, whereby users append a new keyword to specify the context of a query, and a sliding action, whereby users replace a keyword to change the query context. The word map is unique in that it recommends queries for both narrowing and sliding transitions by computing the directional relevance between the input keyword and another keyword in the social knowledge base. The system is applicable to existing search engine query logs, social networking services, and web browsers, enabling users to control the term recommendations by selecting the logs to be analyzed.</div>

