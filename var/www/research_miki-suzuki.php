<h4>顔印象分析によるインタラクティブな化粧手法推薦機構の実現</h4>
<p>本研究の目的は、日常的な化粧の場において、利用者個人の趣味・嗜好に応じた化粧方法を自動的に推薦することにより、メイク支援を実現する点にある。具体的には、色づけのプロセスごとに変化する顔の印象を自動的に分析し、その分析結果に応じた新たなメイクアップイメージを推薦する、スマート・メイクアップ・システムを提案する。</p>
