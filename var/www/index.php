<h2>Interactive Multimedia Environment Lab.の考え方</h2>
<div class="section">
<h4>現在のWebの主役は、動画像、音楽、ソーシャルネットワークに代表される、動きのあるメディア、すなわち、ダイナミックメディアです。 </h4>
