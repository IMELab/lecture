<h4>Personalized Lifestyle Recommender Framework by Meta-Level System for Integrating Heterogeneous e-Commerce Sites</h4>
<p>
本研究の目的は、情報推薦システムにおいて、既存のeコマースサイトを動的に統合することにより（手段）、従来の商品・サービス単体での情報推薦に止まらず（既存研究）、推薦情報群からなる総合的なライフスタイル全体を利用者に提案することである（結果）。
本研究の特徴は、趣味・嗜好を決定づけるものとして、利用者の生活のタイムラインに着目し、生活のタイムラインが類似する集団の共起性を一つの分析要素として情報推薦を行うことである。これに合わせて、本研究のeコマースサイト統合という特色から、カテゴリーの異なる商品・サービスを飛躍して情報推薦することが可能となり、より利用者の潜在的嗜好を喚起することができる。
本研究の利点は、個人化機能や情報推薦機能を持たないeコマースサイトが、本フレームワークに統合されることにより、自動的に個人化機能を有することが出来る点である。
</p>
